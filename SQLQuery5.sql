CREATE PROCEDURE insertNumsAndReverseIfIdMatch (@num1 INT, @num2 INT)
AS
BEGIN
    DECLARE @id INT
    INSERT INTO nums (num1, num2) VALUES (@num1, @num2)
    SET @id = SCOPE_IDENTITY()
    IF @id = @num1
        INSERT INTO nums (num1, num2) VALUES (@num2, @num1)
END


